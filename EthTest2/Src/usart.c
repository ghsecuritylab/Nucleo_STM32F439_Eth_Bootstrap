// UART receive interrupt callback
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if ( huart->Instance == USART2 ) {
	  //HAL_GPIO_TogglePin(GPIOE, LD4_PE8_Pin );
	  // add received char to ring buffer.
	  rb_put(&uart2_rx_rb, uart2_tmp_rx_char );
	  // kick off the next read
	  HAL_UART_Receive_IT(&huart2, &uart2_tmp_rx_char, 1 );
  }
  // other serial ports not handled by this code base..
  // duplicate above for other UARTS to enable them.
}

// handle put_char requests
// send chars using huart2
// allows printf() to work
int __io_putchar(int ch) {
	if ( ch == "\n") {
		// send \r with \n
		char d = '\r';
		HAL_UART_Transmit(&huart2, &d, 1, 1000);
	}
	uint8_t *data;
	data = (uint8_t*)&ch;
	HAL_StatusTypeDef status = HAL_UART_Transmit(&huart2, data, 1, 1000);

	return (status == HAL_OK ? 1 : 0);
}

// handle get_char request
// get chars using huart2 ring buffer as the source.
// allows scanf() to work.
char __io_getchar() {
	// poll until there is data to return (blocks)
	while( rb_isempty(&uart2_rx_rb) == 1 ) { };
	char *dptr;
	dptr = (char *)rb_get(&uart2_rx_rb);
	char data = *dptr;
	return data;
}

